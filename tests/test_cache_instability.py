class CacheInstabilityVolatileImportTests(unittest.TestCase):

    def test_volatile_import_path_triggers_finding(self):
        content = "@import 'status-report.md'\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        self.assertTrue(any(f["name"] == "cache_instability" for f in findings))

    def test_import_with_log_in_name_triggers_finding(self):
        content = '@import "daily-log.md"\n' + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        self.assertTrue(any(f["name"] == "cache_instability" for f in findings))

    def test_stable_import_path_does_not_trigger(self):
        content = "@import 'conventions.md'\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        import_findings = [f for f in findings if "import" in f.get("evidence", "").lower()]
        self.assertEqual(import_findings, [])


class CacheInstabilityShortCircuitTests(unittest.TestCase):

    def test_empty_content_returns_no_findings(self):
        self.assertEqual(detect_cache_instability(_make_session("")), [])

    def test_short_content_under_200_chars_returns_no_findings(self):
        self.assertEqual(detect_cache_instability(_make_session("# tiny")), [])

    def test_missing_claude_md_key_returns_no_findings(self):
        self.assertEqual(detect_cache_instability({}), [])

    def test_stable_config_no_timestamps_no_findings(self):
        content = _big_block(200)
        self.assertEqual(detect_cache_instability(_make_session(content)), [])


