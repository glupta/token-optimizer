"""Tests for detectors/cache_instability.py — detect_cache_instability."""

import sys
import os
import unittest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "skills", "token-optimizer", "scripts"))

from detectors.cache_instability import detect_cache_instability


def _make_session(content):
    return {"claude_md_content": content}


def _big_block(n_lines=200):
    """Return a multi-line stable block of prose (no timestamps, no markers)."""
    return "\n".join(f"Rule {i}: do not do the bad thing and always do the good thing." for i in range(n_lines))


class CacheInstabilityTimestampTests(unittest.TestCase):

    def test_iso_datetime_in_prefix_triggers_finding(self):
        content = "# Config\nUpdated at 2026-06-22T14:30 UTC\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        names = [f["name"] for f in findings]
        self.assertIn("cache_instability", names)

    def test_day_month_date_in_prefix_triggers_finding(self):
        content = "# Notes\nMon, June 22\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        self.assertTrue(any(f["name"] == "cache_instability" for f in findings))

    def test_updated_label_date_in_prefix_triggers_finding(self):
        content = "# My Config\nUpdated: 2026-06-20\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        self.assertTrue(any(f["name"] == "cache_instability" for f in findings))

    def test_last_updated_in_prefix_triggers_finding(self):
        content = "# My Config\nLast updated: 2026-06-01\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        self.assertTrue(any(f["name"] == "cache_instability" for f in findings))

    def test_timestamp_only_in_suffix_no_finding(self):
        # Timestamp in the last 40% → should NOT trigger
        suffix_line = "Updated at 2026-06-22T14:30 UTC"
        stable_block = _big_block(200)
        content = stable_block + "\n" + suffix_line
        findings = detect_cache_instability(_make_session(content))
        self.assertEqual(findings, [])

    def test_finding_includes_savings_tokens_and_occurrence_count(self):
        content = "Updated: 2026-06-01\n" + _big_block(150)
        findings = detect_cache_instability(_make_session(content))
        ts_findings = [f for f in findings if "cache_instability" == f["name"]]
        self.assertTrue(ts_findings)
        f = ts_findings[0]
        self.assertGreater(f["savings_tokens"], 0)
        self.assertGreaterEqual(f["occurrence_count"], 1)
        self.assertGreater(f["confidence"], 0)


